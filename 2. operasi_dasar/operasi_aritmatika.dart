void main() {
  //operasi aritmatika
  var a = 1;
  var b = 5;

  //penjumlahan
  print(a + b);

  //pengurangan
  print(a - b);
  
  //perkalian
  print(a * b);
  
  //pembagian
  print(a / b);
  
  //sisa pembagian
  print(a % b);

  //increment
  print(a++);

  //decrement
  print(a--);

  print(a += b);
  print(a -= b);
}